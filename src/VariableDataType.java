public class VariableDataType {
    public static void main(String[] args) {
        int i = 10;
        float f = 20.5f;
        double d = 20.5;
        boolean b = true;
        char c = 'a';
        String s = "Hà Nội";
        System.out.println("Giá trị của biến i (int): " + i);
        System.out.println("Giá trị của biến f (float): " + f);
        System.out.println("Giá trị của biến d (double): " + d);
        System.out.println("Giá trị của biến b (boolean): " + b);
        System.out.println("Giá trị của biến c (char): " + c);
        System.out.println("Giá trị của biến s (String): " + s);

    }
}
